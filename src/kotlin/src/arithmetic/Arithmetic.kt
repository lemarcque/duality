package arithmetic


// Addition
fun add(a: Number, b: Number) = a.toDouble() + b.toDouble()

// Substraction
fun substract(a: Number, b: Number) = a.toDouble() - b.toDouble()

// Multiplication
fun multiply(a: Number, b: Number) = a.toDouble() * b.toDouble()

// Divison
fun divide(a: Number, b: Number) = a.toDouble() / b.toDouble()

